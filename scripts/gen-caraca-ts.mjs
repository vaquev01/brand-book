/**
 * Generates src/lib/caraca-bar-example.ts from the JSON file.
 * Inlining as TypeScript avoids webpack JSON.parse bundling issue in production.
 * Run: node scripts/gen-caraca-ts.mjs
 */
import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const jsonPath = join(__dirname, "../src/lib/caraca-bar-brandbook.json");
const tsPath = join(__dirname, "../src/lib/caraca-bar-example.ts");

const json = JSON.parse(readFileSync(jsonPath, "utf-8"));

const tsContent = `import { BrandbookData } from "./types";

// Auto-generated by scripts/gen-caraca-ts.mjs
// Inlined as TypeScript object to avoid webpack JSON.parse bundling issue in production.
// Do NOT edit manually — edit caraca-bar-brandbook.json and re-run the script.
export const caracaBarExample = ${JSON.stringify(json, null, 2)} as unknown as BrandbookData;
`;

writeFileSync(tsPath, tsContent, "utf-8");
console.log("✓ Generated src/lib/caraca-bar-example.ts (" + Buffer.byteLength(tsContent) + " bytes)");
